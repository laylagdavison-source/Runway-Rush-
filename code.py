wall_index = 0 # Index for obstacle sprite
wall_sprite: Sprite = None # Sprite variable for obstacles
round_type = 0 # Binary flag for Collectable or Obstacle round
def on_update_interval():
    global round_type
    # If collectable round
    if round_type == 0:
        global collectable_index, collectable_sprite
        # Gets random collectable sprite that is not in last_two_sprites
        collectable_index = randint(0, len(collectables) - 1)
        while last_two_sprites.index_of(collectable_index) >= 0:
            collectable_index = randint(0, len(collectables) - 1)
        # Adjusts the last_two_sprites to match
        last_two_sprites[1] = last_two_sprites[0]
        last_two_sprites[0] = collectable_index
        # Sets the position and speed
        collectable_sprite = collectables[collectable_index]
        collectable_sprite.set_position(scene.screen_width() + 16,
            randint(16, scene.screen_height() - 16))
        collectable_sprite.vx = bgSprite1.vx
    # If obstacle round
    else:
        global wall_index, wall_sprite
        # Grabs the correct obstacle
        wall_sprite = walls[wall_index]
        # Sets obstacle position and velocity
        wall_sprite.set_position(scene.screen_width() + 16,
                randint(16, scene.screen_height() - 16))
        wall_sprite.vx = bgSprite1.vx
        # Updates the obstacle index
        wall_index = (wall_index+1)%4
    # Updates the round_type to alternate
    round_type = (round_type+1)%2
game.on_update_interval(1500, on_update_interval)

